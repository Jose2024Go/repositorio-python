class Cafetera:
    def __init__(self, *args):
        if len(args) == 0:
            self._capacidadMaxima = 1000
            self._cantidadActual = 0
        elif len(args) == 1:
            self._cantidadActual = args[0]
            self._capacidadMaxima = self._cantidadActual
        elif len(args) == 2:
            self._capacidadMaxima = max(args[0], args[1])
            self._cantidadActual = min(args[0], args[1])

    def get_cap_max(self):
        return self._capacidadMaxima

    def get_cant_actual(self):
        return self._cantidadActual

    def servir_taza(self, cant):
        cant_nueva = self._cantidadActual - cant
        if cant_nueva >= 0:
            self._cantidadActual = cant_nueva
        else:
            cant = self._cantidadActual
            self._cantidadActual = 0

    def llenar_cafetera(self):
        self._cantidadActual = self._capacidadMaxima
        print("Cafetera llena.")

    def vaciar_cafetera(self):
        self._cantidadActual = 0
        print(f"La cafetera está vacía. Tiene {self._cantidadActual} cc de café.")

    def agregar_cafe(self, cafe):
        cant_nueva = self._cantidadActual + cafe
        self._cantidadActual = min(cant_nueva, self._capacidadMaxima)
        print(f"La cafetera tiene actualmente {self._cantidadActual} cc de café.")

    def mostrar(self):
        print(f"La cafetera tiene capacidad de {self._capacidadMaxima} cc y actualmente {self._cantidadActual} cc de café.")


cafetera1 = Cafetera()
cafetera1.mostrar()
cafetera1.llenar_cafetera()
cafetera1.mostrar()
cafetera1.servir_taza(1000)
cafetera1.mostrar()
