class Pila():
    def __init__(self):
        self.datos=[]

    def push(self, dato):
        self.datos.append(dato)
        print(f"El elemento {dato} ha sido apilado")

    def pop(self):
        if not self.datos.vacia:
            self.datos.pop()
            print("El elmento ha sido desapilado")
        else:
            print("La pila ya se encuentra vacia")
        return()
    def vacia(self):
        if len(self.datos)==0:
            return()

