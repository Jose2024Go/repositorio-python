class Empleado:
    def __init__Empleado(self):
        self.nombre = ''
        self.apellido = ''
        self.seueldo1 = ''
        self.seueldo2 = ''
        self.seueldo3 = ''
        self.seueldo4 = ''

    def leer(self):
        self.nombre = input('Ingrese el nombre:')
        self.apellido = input('Ingrese el apellido:')
        self.seueldo1 = int(input('Ingrese el sueldo 1:'))
        self.seueldo2 = int(input('Ingrese el sueldo 2:'))
        self.seueldo3 = int(input('Ingrese el sueldo 3:'))
        self.seueldo4 = int(input('Ingrese el sueldo 4:'))

    def mostrar(self):
        print(self.nombre, self.apellido)

    def sacarPromedio(self):
        suma = self.seueldo1 + self.seueldo2 + self.seueldo3 + self.seueldo4
        promedio = suma/4
        print(f'el promedio de los sueldos es:',(promedio))

