# 1. Crear una lista de números del 1 al 10
numeros = list(range(1, 11))
print("🎩 Lista inicial:", numeros)

# 2. Agregar los números 11 y 12 al final de la lista
numeros.extend([11, 12])
print("✨ Añadimos 11 y 12:", numeros)

# 3. Eliminar el número 5 de la lista (¡Adiós 5!)
numeros.remove(5)
print("❌ Eliminamos el número 5:", numeros)

# 4. Calcular la suma de los elementos
suma = sum(numeros)
print("➕ Suma de los elementos:", suma)

# 5. Encontrar el número máximo y mínimo
maximo = max(numeros)
minimo = min(numeros)
print("🔝 Número máximo:", maximo)
print("🔻 Número mínimo:", minimo)

# 6. Ordenar la lista en orden ascendente
numeros.sort()
print("📈 Lista ordenada:", numeros)

# 7. Mostrar la lista final y los resultados
print("--------------------RESULTADO--------------------")
print("🎉 Lista final:", numeros)
print("🎊 Suma de los elementos:", suma)
print("🏆 Número máximo:", maximo)
print("🥇 Número mínimo:", minimo)

