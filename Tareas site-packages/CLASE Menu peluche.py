class Peluche():
    def __init__(self, tipo, size, color):
        self.tipo = tipo
        self.size = size
        self.color = color


i = 0
lista = []


def mostrar():
    k = 0
    while k < len(lista):
        print(lista[k].tipo, " ", lista[k].size, " ", lista[k].color)
        k += 1
def mostrar_peluches_grandes(peluches):
    for peluche in peluches:
        if peluche.size == "grande":
            print(peluche.tipo, " ", peluche.size, " ", peluche.color)

while i == 0:
    print('MENU')
    print('1. Registrar persona')
    print('2. Consultar lista')
    print('3. Salir')
    opcion = int(input())
    if opcion == 1:
        print('******Registrar*****')
        n = input('Ingrese el tipo de peluche:  ')
        t = input('Ingrese el tamaño del peluche:  ')
        u = input('Ingrese el color del peluche:')
        per1 = Peluche(n, t , u)
        lista.append(per1)

        print('Peluche guardado con exito')
    elif opcion == 2:
        print('******Mostrar*******')
        mostrar()
        mostrar_peluches_grandes(lista)
    elif opcion == 3:
        exit()
    else:
        print('Opción inválida')
