class Auto:
    #Por argumento
    def __init__(self,*args):
        if len(args)==0:#Por defecto
            self.color = 'blanco'
            self.marca = 'honda'
            self.modelo = 2015
        if len(args)==3:
            self.color = args[0]
            self.marca = args[1]
            self.modelo = args[2]

    def mostrar(self):
        print(self.color,self.marca,self.modelo)

    def leer(self):
        self.color = input('Ingrese color:')
        self.marca = input('Ingrese marca:')
        self.modelo = input('Ingrese modelo:')

carro1 = Auto('amarillo','toyota', 2020)
carro2 = Auto()
carro1.mostrar()
carro2.mostrar()

