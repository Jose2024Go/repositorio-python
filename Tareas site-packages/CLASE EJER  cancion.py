class Cancion:
    def __init__(self, *args):
        if len(args) == 0:
            self.titulo = "rosas"
            self.autor = "nodal"

        elif len(args) == 2:
            self.titulo = args[0]
            self.autor = args[1]

    def dame_titulo(self):
        return self.titulo

    def pon_titulo(self, nuevo_titulo):
        self.titulo = nuevo_titulo

    def dame_autor(self):
        return self.autor

    def pon_autor(self, nuevo_autor):
        self.autor = nuevo_autor

    def mostrar(self):
        print(f"TÃ­tulo: {self.titulo}")
        print(f"Autor: {self.autor}")

# Ejemplo de uso
mi_cancion1 = Cancion()
mi_cancion1.mostrar()

mi_cancion2= Cancion("Yesterday", "The Beatles")
mi_cancion2.mostrar()
mi_cancion1.pon_titulo('cielo')
mi_cancion1.mostrar()


