def contar_vocales(frase):
    frase = frase.lower()
    cont_vocales = 0
    for letra in frase:
        if letra in "aeiou":
            cont_vocales += 1
    return cont_vocales

# Ejemplo de uso
frase_usuario = input("Ingrese una frase: ")
numero_vocales = contar_vocales(frase_usuario)
print(f"El n√∫mero de vocales es: {numero_vocales}")