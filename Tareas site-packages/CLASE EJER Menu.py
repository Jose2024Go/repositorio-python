class Persona():
    def __init__(self, nom, tel):
        self.nom = nom
        self.tel = tel


i = 0
lista = [[2,4,6,8],
         [0,0,0,0]]


def mostrar():
    k = 0
    while k < len(lista):
        print(lista[k].nom, " ", lista[k].tel)
        k += 1


while i == 0:
    print('MENU')
    print('1. Regsitrar persona')
    print('2. Consultar lista')
    print('3. Salir')
    opcion = int(input())
    if opcion == 1:
        print('******Registrar*****')
        n = input('Ingrese el nombre:  ')
        t = input('Ingrese el telefono:  ')
        per1 = Persona(n, t)
        lista.append(per1)
        print('persona guardada con exito')
    elif opcion == 2:
        print('******Mostrar*******')
        mostrar()
