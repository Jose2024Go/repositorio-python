import random
import string
import tkinter as tk
from tkinter import messagebox
from PIL import ImageTk, Image

def generar_contraseña(longitud):
    caracteres = string.ascii_letters + string.digits + string.punctuation
    contraseña = "".join(random.choice(caracteres) for _ in range(longitud))
    return contraseña

def generar_contraseña_click():
    try:
        longitud = int(entry_longitud.get())
        nueva_contraseña = generar_contraseña(longitud)
        messagebox.showinfo("Contraseña generada", f"La nueva contraseña es: {nueva_contraseña}")
    except ValueError:
        messagebox.showerror("Error", "Ingresa una longitud válida (número entero)")

def acerca_de():
    messagebox.askquestion("Acerca de", "Desarrollado por Daniel, Jose, Antohony, James y Brisheyda")

def salir():
    resp = messagebox.askquestion("Salir", "¿Desea salir?")
    if resp == 'yes':
        ventana.destroy()

# Crear ventana principal
ventana = tk.Tk()
ventana.title("Generador de Contraseñas")
ventana.geometry("1060x566")
fondo = ImageTk.PhotoImage(file='C:\\CURSOS PYTHON\\ejercicios\\pythonProject1\\sistemas.gif')
fondo1 = tk.Label(ventana, image=fondo)
fondo1.place(x=0, y=0)

barra_menu = tk.Menu(ventana)
ventana.config(menu=barra_menu)

Inicio = tk.Menu(barra_menu, tearoff=0)
barra_menu.add_cascade(label="Inicio", menu=Inicio)
Inicio.add_command(label="Cerrar", command=salir)

Ayuda = tk.Menu(barra_menu, tearoff=0)
barra_menu.add_cascade(label="Ayuda", menu=Ayuda)
Ayuda.add_command(label="Datos", command=acerca_de)

# Etiqueta y campo de entrada para el nombre del usuario
label_nombre_usuario = tk.Label(ventana, text="Ingresar usuario:")
label_nombre_usuario.place(x=10, y=10, width=250, height=30)
entry_nombre_usuario = tk.Entry(ventana)
entry_nombre_usuario.place(x=270, y=10, width=150, height=30)

# Etiqueta y campo de entrada para la longitud
label_longitud = tk.Label(ventana, text="¿Cuál es la longitud de la contraseña deseada?")
label_longitud.place(x=10, y=50, width=250, height=30)
entry_longitud = tk.Entry(ventana)
entry_longitud.place(x=270, y=50, width=150, height=30)

# Botón para generar contraseña
boton_generar = tk.Button(ventana, text="Generar Contraseña", fg="yellow", bg="blue", command=generar_contraseña_click)
boton_generar.place(x=10, y=90, width=250, height=30)

ventana.mainloop()

