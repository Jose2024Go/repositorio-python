#Se creo una funcion que se llama shell
def shell(lista):
    #Se divide entre para obtener un intervalo inicial
    intervalo = len(lista)//2

    #Mientras el intervalo sea maor a 0, se sigue ordenando.
    while intervalo > 0:
        #Se llama a la funcion 'reducir_busqueda' para cada sublista.
        for p in range(intervalo):
            reducir_busqueda(lista , p , intervalo)
        #Se reduce el intervalo a la mitad para la proxima iteracion.
        intervalo = intervalo//2

def reducir_busqueda(lista,inicio,salto):
    #Se recorre la lista desde 'inicio + salto' hasta el final,con pasos de 'salto'.
    for i in range(inicio + salto , len(lista) , salto):
        #Se guarda el valor actual y su posicion.
        valor = lista[i]
        posicion = i
        #Mientras la posion sea adecuada, se intercambian valores para ordenar.
        while posicion >= salto and lista[posicion - salto] > valor:
            lista[posicion] = lista[posicion - salto]
            posicion = posicion - salto
        #Se coloca el valor de su posicion ordenada.
        lista[posicion] = valor

#Solicitar al usuario el rango n para lista
n = int(input("Ingrese el numero de elementos en la lista:"))
#Crear una lista vacia para almacenar los valores
lista = []
#Solicitar al usuario que ingrese los valores de la lista
for i in range(n):
    valor = int(input(f"Ingrese el valor para el elemto {i+1}:"))
    lista.append(valor)

print("Antes de ordenar:")
print(lista)
print("Despues de ordenar:")
#Se llama la funcion 'shell' para ordenar la lista
shell(lista)
