def contar_palabras(frase):
    espacios = 0
    cantidad = len(frase)

    for i in range(cantidad):
        if frase[i] == " ":
            espacios += 1

    # Una frase tendr√° siempre tantas palabras como espacios
    palabras = espacios + 1
    return palabras

# Solicitar al usuario que ingrese una frase
frase_usuario = input("Ingrese una frase: ")

# Calcular la cantidad de palabras en la frase
cantidad_palabras = contar_palabras(frase_usuario)

# Mostrar el resultado
print(f"La frase tiene {cantidad_palabras} palabras.")